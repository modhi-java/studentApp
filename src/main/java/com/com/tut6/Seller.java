package com.com.tut6;

public class Seller {

    private Integer codet;
    private String namet;
    private String jawal;

    public Integer getCodet() {
        return codet;
    }

    public void setCodet(Integer codet) {
        this.codet = codet;
    }

    public String getNamet() {
        return namet;
    }

    public void setNamet(String namet) {
        this.namet = namet;
    }

    public String getJawal() {
        return jawal;
    }

    public void setJawal(String jawal) {
        this.jawal = jawal;
    }
}
